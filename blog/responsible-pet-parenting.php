 					<p>Having a pet is a wonderful thing but it also comes with a lot of responsibility. We live in a society where it is necessary to adhere to rules else, trouble is sure to follow!  In all the excitement of keeping a pet, people forget that it comes with a great deal of responsibility to both society, family and the pet itself. This article is about the duties of pet parents towards society.</p>

					<p>Do you walk your dog on a leash?  There are many people with a very genuine fear of animals due to previous trauma. For a dog, a trigger could be a cat, a person they do not like or a loud noise that can cause him to bolt or cause harm.  The chaos that follows could prove dangerous for both pet and humans around. If you cannot imagine it, let me give you a scenario.  Your unleashed dog spots a cat, he chases it and in his excitement knocks down a child in his way. When in public, you have a great responsibility towards your fellow citizens. It is safer to keep your dog on a leash in open areas and let him be off leash in enclosed areas like parks. It is also advisable to teach him important commands like 'come' even if there is a distraction so you have the confidence to control him if he decides to dart off.</p>

					<p>Does your dog bark when he is awake? This could be noise pollution to your neighbours espically when it happens at night.  Please don't take it for granted.  Get a trainer to teach your dog to stop barking on command.  A trained and well-behaved dog is a happy dog and will ease your life too.</p>

					<p>What about pooping in the society or on the road? Remember to carry newspaper with you or a poop scooper to pick up after your dog.  Hygiene is to be followed everywhere and every day.  There is no excuse for your dog to litter in a place and then not clean it up.</p>

					<p>Ok, so you don't have a pet at home but you have pets in the society parking lot, garden and on the roads.  You feed them biscuits and chicken rice as well, correct????   Well, there are rules there too.  Designate an area and time.  Take the animals to an isolated area where people are not gathered and feed them there peacefully.  Avoid feeding when kids are playing and cycling all around.  Dogs can get territorial and attack.  After you are done, please clean up.  If you feed on newspaper, pick it up and throw it away.</p>

					<p>As far as building elevators are concerned, societies cannot make rules that forbid you the right to use it.  In return you have to make sure your pet does not soil the area.  If he does out of urgency, be responsible to clean up after your pet and preserve the hygiene of the area.  If you need to use the elevator and you have a hyper, jumpy dog then get him trained or else people may complain about his manners.</p>

					<p>If you are doing everything in your capacity to ease the minds of your neighbours and society, then there is nothing that can go wrong for you.  If you face harassment despite it, then you can approach your local NGO who will connect you to an animal welfare officer in your area to help deal and clear any misunderstandings with your building secretary and chairman.</p>
                    
                    <p>Understand that not everyone is tolerant of animals but with a little extra effort, you can ease things out and ensure that a healthy man-animal relationship is developed and respected.  You never know how many people will be convinced about the goodness of animals and pets only because of your efforts and behavior.</p>
                    
