					<center><img src="/images/blogs/spay.png" alt="Spaying or neutering your pet" class="img-responsive margin-bottom-30"></center>
                        
 					<p>Spaying or neutering is the same as sterilization. The word spay is used for the male species and neuter for the female. Now there are many myths that surround spaying and neutering. The fact of the matter is that sterilization makes your dog a healthier and better behaved pet. It will also save you money in the long run.</p>

					<p>You can understand the concept of sterilization only if you stop equating your feelings with that of your pet. It will not make them any less male or female. Males when neutered do not mark their territory and thus remain non-dominant, peaceful and less adventurous. When a female dog comes in heat she goes through hormonal changes and will also attract unwanted male dogs.</p>

					<p>Understand that animals are programmed to reproduce to ensure survival of their species. For them it is not about having kids and grandkids. If you feel that sterilization is an expensive matter well, think again. There are NGOs that can help you with that. Females experience false pregnancies and uterus infections that can be fatal. Neutering will also eliminate the possibility of uterine or ovarian cancer. In males, the risk of prostate cancer is greatly reduced.</p>

					<p>After sterilization continue to feed your pet a healthy and balanced diet and provide him/her with an effective walking and exercise routine. Otherwise, sterilized or not, you will find yourself stuck with fat and lazy pets.</p>

					<p>Now, if you want to breed your female pets, let me give you a couple of things to ponder over.</p>
                    
                                        <ul>
                                            <li><i class="icon-check-square"></i> Firstly, are you breeding her because your city or country lacks dogs and cats?</li>
                                            <li><i class="icon-check-square"></i> Second, do you just want to play with pups and kittens?</li>
                                            <li><i class="icon-check-square"></i> Third, do your friends want puppies and kittens?</li>
                                            <li><i class="icon-check-square"></i> Fourth, do you want to sell the babies and make money from her?</li>
                                        </ul>

                    <p></p>

					<p>There are more than enough breeders all over the world. Also aren't there enough back yard breeders running puppy mills? Illegal breeding of dogs is one of the worst crimes committed against animals today.</p>

					<p>Suppose you just want to see little miniatures of your pet. You also have 5 friends who are crazy animal lovers who will adopt all the 5 pups, thus all will live happily ever after! Well, there too, I can give you a valid point to deter you not to breed your dog. If your 5 friends can adopt 5 dogs from an animal shelter they will be giving abandoned dogs a second chance at life and emptying the shelter for new dogs to be rescued.</p>

					<p>Be a responsible pet parent and spay or neuter your pet. Both psychologically and biologically, it is the best thing for your dog or cat. If there is no need to breed your pet then you will save many innocent animals the pain of a very uncertain and painful future.</p>

