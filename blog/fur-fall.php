 					<p>If you think Rapunzel didn't have hair fall, you're wrong! All of us have hair fall including our pets. Hair is generally shed when another hair is ready to replace it. This is part of the hair's growth cycle. The hair or fur in animals is all connected to nerves, and so the fur also serves as a transmitter for sensory input. It protects their skin from wounds, bites, heat, cold and UV radiation. It is also used as a communication tool and as a camouflage.</p>

					<p>Some dog and cat breeds shed through the year while shed-resistant dogs have fur fall occasionally. While it is apparent that every dog is going to shed irrespective, a pet who is shedding more than the usual amount may have a health issue. The overall health and nutrition of the dog are one of the key factors in all of this. Let us see how you can reduce it by keeping your babies healthy and well-groomed.</p>

					<p>Start with a high quality diet with natural good quality fats. It is a remedy for a lot of other healthy issues as well. If the food is filled with nutrients then it will control shedding and eliminate dry skin to a certain extent. Be informed about your pet's food allergies. Check with your vet for the right vitamin and 3 dietary supplements. Check for an hormonal imbalances also. A blood test generally gives a fair idea. Make sure your they have access to clean, fresh water daily because dehydration can lead to dry skin, which can cause excessive shedding.</p>

					<p>Grooming your pet is another activity that should be done regularly. It will also help you strengthen the bond between the two of you. Grooming is done to remove excess and loose fur. As you pass a brush through the fur, skin oils also get redistributed. Find out what kind of fur your pet has. Then accordingly choose a bristle, slicker or rake brush.  Dogs and cats with short coats require a rubber curry comb to de-shed.</p>

					<p>Do not ignore or postpone your dog's bath. Cats normally do not need bathing. Body odour is a good indication. A bath will allow loose hair to fall during the process instead of on your furniture. Again do not over do it because it can cause dry skin, which reverses all your good effort and causes fur to fall out. Towel dry your pet nicely and then blow dry on the lowest heat or cool setting.</p>

					<p>Make sure the fur fall that you see is not due the vigorous scratching that comes from fleas and ticks. Please ensure that you inspect your pets regular and see to an appropriate treatment with your vet's guidance. Keeping them clean and free of parasites will prevent irritated skin, dandruff and excessive fur shedding.</p>

					<p>Stress too causes fur fall and if you notice it you may be able to reverse a lot of unforeseen problems and expenses. Any, change in the routine or body functions of your pet warrants a vet check up. Please do not ignore it. Your pet is dependant on you for its well being. If you neglect and ignore it, fur fall will be only of the major problems that you will have on hand.</p>

