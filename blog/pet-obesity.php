 					<p>In a country of fast food and fast weight, it is not only the people who stand a risk of obesity but our pets as well. Today, a majority of pets continue to be overweight, and most owners aren't even aware of the problem. If you think a fat dog or cat is cute then think again.</p>

					<p>Obesity can be defined as an excess of body fat that will eventually cause unnecessary suffering and permanent disabling damage. There are a couple of factors that increase the risk of obesity such as the breed, age, medical conditions etc. If these factors are not the reason for your pet's excess weight then this life threatening reality can be blamed on carelessness, a poor diet and lack of exercise.</p>

					<p>Obesity causes diabetes, heart disease, respiratory distress, high blood pressure and cancer. It also affects the bones and joints. The natural behavior of the animal is impacted as well. For example, a hyper breed like a beagle should ideally be playing and running around but when obese, it can only walk for a short while. What comes naturally to the dog is impaired due to the excess weight. It can sometimes, turn a loving and good-tempered dog into an irritable one putting everyone at home at risk. The good news is, obesity is preventable and reversible.</p>

					<p>Ideally, for dogs and cats you should be able to see and feel the outline of their ribs without excess fat covering. Take a moment to go and check your pets. If the opposite is true then see your vet as soon as possible and ask them to help you get your pet back in shape. Monitor and strive to maintain the desired weight for life. Reduce high calorie foods and include more fiber.</p>

					<p>Give your family members strict instructions on foods allowed. Do not encourage unwanted snack feeding and table scraps. The entire family will have to be united in this effort. If you give your pet packaged food then do some market research to determine a good brand. Most packaged foods contain a lot of fat that make the food tasty for your pet but they are unhealthy in the long run. Do not take the easy way out and just decrease the amount of food that you currently feed. This will cause malnourishment, deficiencies and another set of problems.</p>

					<p>If you give your pet home cooked food then make sure you are giving them healthy, nutritious food. Use brown/red rice instead of white rice, add vegetables and fruits to their diet and give them healthy snacks like steamed sweet potato, carrot sticks or homemade baked treats.</p>

					<p>Walk your dogs for at least 30 minutes, twice a day and include physical activities like swimming, trekking. You could also playing simple games like 'fetch' early in the morning in an outdoor, safe area. It will keep both of you in good health. If you have a cat, then give it some interesting toys to play with and run around the house. Old pets will not be able to exercise so seek a diet that strikes a balance between your pet's energy intake and its usage.</p>

					<p>Love does not mean fattening someone up to look cute. Love means having to say 'No' sometimes. Even if your pet insists, you have to be firm in your commitment to its health and weight. Your furbaby may not understand this but you do and you have been entrusted with this precious and innocent life. Our pets have such short lives and I am sure you don't want to lose them to obesity.</p>

