				<article>
					<p>Owning a dog is a wonderful thing. There is no substitute for their unconditional love and friendship. No matter what your status in life, having a dog for companion is one of the best choices you can ever make. Having said that, how do you decide what breed of dog suits you best?</p>

					<p>Before we go any further you have to decide on the age of your pet. Puppies are a handful. Simply put, they are four legged babies! They need potty training, socializing, vet visits, special diets and lots of play time and attention.</p>

					<p>Adult dogs on the other hand are house trained, require to be taken down twice a day, their meals can be left for them and vet visits are reduced to once or twice a year. Love and attention whenever possible is perfectly fine. Most adult dogs like to laze around the house so you do not have to feel guilty about not being there most of the day.</p>

					<p>Your choice of breed will depend on the following things:</p>

                                        <ul>
                                            <li><i class="icon-check-square"></i> Your own physical and mental well-being.</li>
                                            <li><i class="icon-check-square"></i> Do you have very young kids or elderly people around the house? Their allergies to pets.</li>
                                            <li><i class="icon-check-square"></i> Your income.</li>
                                            <li><i class="icon-check-square"></i> The size of your home. Is it small or spacious with an attached garden?</li>
                                            <li><i class="icon-check-square"></i> Are you going to be out for long hours? Vacation time?</li>
                                            <li><i class="icon-check-square"></i> Do you have a reliable person to help in your absence?</li>
                                            <li><i class="icon-check-square"></i> Will you walk your dog or do you have to find a dog walker?</li>
                                            <li><i class="icon-check-square"></i> Grooming? Can you handle all the drama that comes with ‘FUR’?</li>
                                        </ul>

                                        <p></p>

					<p>Large breed dogs are most suitable for large spaces. They can adapt to a small space but they are happiest when they have room to roam comfortably and play as well. That is why they laze around through the day so that when its play time, they can spend their energy. Many large breed dogs have good temperaments and along with their easy going nature are also possessive of their families. ‘LARGE’ means large everything! Larger amounts of food, space, heavier bodies and increased costs (vet bills and travelling).</p>

					<p>Small breeds again mean everything on a small scale. Food, space, weight, vet bills and traveling costs. With today's urban lifestyle, a small dog is best suited to the space crunch. Small dog breeds are known for their fickle mindedness. They tend to snap and growl at dogs larger than them and throw tantrums at owners who let them have their way due to the fact that they are “SO CUTE”!</p>

					<p>Some small breed dogs cost a bomb. They may also need ample walking and exercise time, so do not let size fool you. Lastly think about whether you want a furry or non-furry dog. Grooming requires time and energy. And if you cannot do it then it requires money for regular maintenance. Fur could also pose health issues for you and your family members.</p>

					<p>Whatever your decision, try to adopt. Spread the word and your will surely find something through animal networking sites and your local rescuers. If you buy the breed of your choice then please buy from a responsible breeder.</p>

					<p>Remember that your dog will live for at least 10 years if not more. Be prepared for both an emotional and financial commitment, come what may. Dogs are like children who never grow up so they will be dependent on you right throughout their lives. There is no exchange, return or refund once you become a pet parent!</p>
                    
                    <p align="right">Rohini Fernandes<br>June 2015</p>

